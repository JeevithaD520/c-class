#include<iostream>
using namespace std;

int main()
{
int a;
cout<<"enter the number:"<<endl;
cin>>a;
switch(a)
{
case 1:
cout<<"monday"<<endl;
break;
case 2:
cout<<"tuesday"<<endl;
break;
case 3:
cout<<"wednesday"<<endl;
break;
case 4:
cout<<"thursday"<<endl;
break;
cout<<"friday"<<endl;
break;
cout<<"saturday"<<endl;
break;
cout<<"sunday"<<endl;
break;

default:
cout<<"it is not the day"<<endl;
break;
}
}


